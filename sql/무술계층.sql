        심법_이미지.img_path AS 심법_이미지_url,
        유파_이미지.img_path AS 유파_이미지_url
      FROM T_심법 s
      LEFT JOIN T_이미지 심법_이미지 ON s.img = 심법_이미지.id
      LEFT JOIN T_이미지 유파_이미지 ON s.유파_code = 유파_이미지.code
      ORDER BY s.유파_code, s.순서 ASC, s.created_at DESC


-- View 사용 예시 쿼리

-- ============================================
-- 1. 특정 빌드의 무술 목록 조회
-- ============================================
SELECT * FROM `V_빌드보드_무술` WHERE 빌드보드_id = 1;

-- ============================================
-- 2. 특정 빌드의 심법 목록 조회
-- ============================================
SELECT * FROM `V_빌드보드_심법` WHERE 빌드보드_id = 1;

-- ============================================
-- 3. 특정 빌드의 비결 목록 조회
-- ============================================
SELECT * FROM `V_빌드보드_비결` WHERE 빌드보드_id = 1;

-- ============================================
-- 4. 빌드 전체 정보를 한 번에 조회 (JSON 형태)
-- ============================================
SELECT * FROM `V_빌드보드_전체` WHERE id = 1;

-- JSON 파싱 예시 (MySQL 5.7+)
SELECT 
  id,
  name,
  JSON_EXTRACT(무술들, '$[0].id') AS 첫번째_무술_id,
  JSON_LENGTH(심법들) AS 심법_개수,
  JSON_LENGTH(비결들) AS 비결_개수
FROM `V_빌드보드_전체`
WHERE id = 1;

-- ============================================
-- 5. 모든 빌드의 요약 정보 조회
-- ============================================
SELECT 
  id,
  name,
  version,
  status,
  JSON_LENGTH(무술들) AS 무술_개수,
  JSON_LENGTH(심법들) AS 심법_개수,
  JSON_LENGTH(비결들) AS 비결_개수,
  created_at
FROM `V_빌드보드_전체`
ORDER BY created_at DESC;

